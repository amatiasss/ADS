bool ltDriver::Attach( char* file ){
    int rc;
    rc = sqlite3_open(file, &db);
    if( rc!=SQLITE_OK ) return true;
    return false;
}
//---------------------------------------------------------------------------
void ltDriver::Detach( void ){
    sqlite3_close(db);
}
